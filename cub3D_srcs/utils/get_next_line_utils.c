#include "../../cub3D_include/cub3D.h"

int	ft_strchr(char *s, int c)
{
	int	i;

	i = 0;
	if (s)
	{
		while (s[i] != '\0')
		{
			if (c == s[i])
				return (1);
			i++;
		}
	}
	return (0);
}
